kiwi_caught = 0;
function process_frame(){
    if (kiwi_rate > Math.random()*100) {
        create_kiwi();
    }
    if (is_down ('b')) {
         brake_truck();
    }
    if (is_down ('left')){
         translate_truck_left();
    }
    if (is_down('right')){
         translate_truck_right();
    }
    coast_truck();
    compute_truck_position();
}


function translate_truck_right(){
    set_truck_velocity(get_truck_velocity() + 10);
    if (get_truck_velocity() > 15){
        set_truck_velocity(5);
    }
}
function translate_truck_left(){
    set_truck_velocity(get_truck_velocity() - 10);
    if (get_truck_velocity() < -15){
        set_truck_velocity(-5);
    }
}
function coast_truck(){
    if(get_truck_velocity() > 5){
        set_truck_velocity(get_truck_velocity() - 2.5);
    }
    if(get_truck_velocity() < -5){
        set_truck_velocity(get_truck_velocity() + 2.5);
    }
}
function brake_truck(){
    while (get_truck_velocity() > 0){
        set_truck_velocity(get_truck_velocity() - 10);
    }
    while(get_truck_velocity() < 0){
        set_truck_velocity(get_truck_velocity() + 1);
    }
}
function compute_truck_position() {
    if (get_truck_left() < 0){
        set_truck_velocity(1);
    }
    if (get_truck_left() > window.innerWidth - 250){
        set_truck_velocity(-1);
    }
    set_truck_left(get_truck_left()+ get_truck_velocity());
}


function find_collisions(kiwi){
	if (get_kiwi_x(kiwi) >= get_truck_left() && get_kiwi_x(kiwi) <= get_truck_left() + 250 && get_kiwi_y(kiwi) >= window.innerHeight- 25){
        delete_kiwi(kiwi);
        kiwi_caught += 1;
        document.getElementById("kiwi-count").innerHTML = kiwi_caught;
    }
    //Because Farmer Dave is broke and needs all the kiwi kash he can get his truck is equipped to catch kiwis wherever they land on his truck and to do this I had to find the endpoints of his truck and the appropriate height for his truck to catch the kiwis so that they wouldn't dissappear too early  or to far away from his truck
}


function game_over(){
    var reset = 0;
    alert('Game Over!', 'Congratulations you have caught '+ kiwi_caught + ' many kiwis', 'New Game');
    document.getElementById("kiwi-count").innerHTML = reset;
    kiwi_caught = reset;
}


